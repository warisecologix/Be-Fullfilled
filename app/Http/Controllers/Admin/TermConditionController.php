     * @return \Illuminate\Http\Response
     */
    public function store(TermConditionRequest $request)
    {
        $termCondition = new TermCondition();
        $termCondition->title = $request->title;
        $termCondition->description = $request->description;
        $termCondition->save();
        return redirect()->route('terms_and_condition')->with('success_message', 'Term & Condition successfully created.');
    }

